const contentDiv = document.getElementById("content");

document.querySelectorAll(".branch-tile").forEach((tile) => {
    tile.addEventListener("click", () => {
        const branch = tile.dataset.branch;
        loadBranchContent(branch);
    });
});

function loadBranchContent(branch) {
    const branchData = branches[branch];
    if (!branchData) return;

    // Clear existing content and add branch-specific data
    contentDiv.innerHTML = `
        <h2 class="text-center text-3xl mb-4">${branchData.title}</h2>
        <div class="semesters-container">
            ${Object.entries(branchData.semesters)
                .map(
                    ([sem, subjects]) => `
                    <div class="semester-card bg-gray-700 p-4 rounded-lg mb-6">
                        <h3 class="text-2xl mb-3">Semester ${sem}</h3>
                        ${subjects
                            .map(
                                (subject) => `
                            <div class="subject flex justify-between items-center mb-2">
                                <p>${subject.name}</p>
                                <a href="${subject.link}" target="_blank" class="download-link text-blue-500 underline">Download</a>
                            </div>
                        `
                            )
                            .join("")}
                    </div>
                `
                )
                .join("")}
        </div>
        <button class="bg-blue-500 text-white py-2 px-4 rounded mt-4" onclick="loadHome()">Go Back</button>
    `;
}

function loadHome() {
    location.reload(); // Reload the page to show the initial content
}
